// Command seedusers inserts a batch of demo users into the `users` table so the
// Admin Portal (GET /admin/users) has lifelike data to browse. Dev/demo only.
//
// The dataset is mapped from a real account export (Kiddilab). Only the fields
// the `users` table has are kept:
//   - full name      -> name
//   - phone          -> phone (masked middle digits filled with random values)
//   - username       -> email (username + "@gmail.com", sanitized; a username
//                       that is already an email is used as-is)
// Everything else from the source (address, order code, the source's own id)
// is dropped; `id` is generated by our system (UUID default).
//
// Two modes:
//
//	# A) Connect directly and insert (local dev DB, or any reachable DB):
//	DATABASE_URL="postgres://wearwhere:wearwhere@localhost:5432/wearwhere?sslmode=disable" \
//	    go run ./cmd/seedusers
//
//	# B) Emit SQL to stdout (for DBs reachable only via psql, e.g. the GCP VM
//	#    where Postgres is internal-only — pipe into the container's psql):
//	go run ./cmd/seedusers -sql | \
//	    ssh -i ~/.ssh/wearwhere-demo deploy@34.87.41.62 \
//	      'docker exec -i wearwhere-postgres-1 psql -U wearwhere -d wearwhere -v ON_ERROR_STOP=1'
//
// Idempotent: re-running skips users whose phone already exists
// (ON CONFLICT (phone) DO NOTHING).
