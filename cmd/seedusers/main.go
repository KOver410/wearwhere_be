func main() {
	emitSQL := flag.Bool("sql", false, "print INSERT statements to stdout instead of connecting to a DB")
	flag.Parse()

	if *emitSQL {
		fmt.Print(buildSQL())
		return
	}
	runDirect()
}

// buildSQL renders the seed as a single transactional SQL script.
func buildSQL() string {
	var b strings.Builder
	b.WriteString("BEGIN;\n")
	for _, u := range users {
		fmt.Fprintf(&b,
			"INSERT INTO users (name, phone, email) VALUES (%s, %s, %s) "+
				"ON CONFLICT (phone) DO NOTHING;\n",
			sqlStr(u.Name), sqlStr(u.Phone), sqlStr(u.Email),
		)
	}
	b.WriteString("COMMIT;\n")
	return b.String()
}

// sqlStr quotes a string as a SQL literal, or returns NULL for the empty string.
func sqlStr(s string) string {
	if s == "" {
		return "NULL"
	}
	return "'" + strings.ReplaceAll(s, "'", "''") + "'"
}

// runDirect connects via DATABASE_URL and inserts the users with parameterized
// queries.
func runDirect() {
	dbURL := os.Getenv("DATABASE_URL")
	if dbURL == "" {
		dbURL = defaultDBURL
		log.Printf("DATABASE_URL not set, using dev default: %s", dbURL)
	}

	ctx := context.Background()
	pool, err := pgxpool.New(ctx, dbURL)
	if err != nil {
		log.Fatalf("connect db: %v", err)
	}
	defer pool.Close()
	if err := pool.Ping(ctx); err != nil {
		log.Fatalf("ping db: %v", err)
	}

	const q = `INSERT INTO users (name, phone, email) VALUES ($1, $2, $3)
	           ON CONFLICT (phone) DO NOTHING`

	inserted, skipped := 0, 0
	for _, u := range users {
		tag, err := pool.Exec(ctx, q, u.Name, u.Phone, u.Email)
		if err != nil {
			log.Fatalf("insert %q: %v", u.Name, err)
		}
		if tag.RowsAffected() == 1 {
			inserted++
		} else {
			skipped++
			log.Printf("skip (phone exists): %s", u.Phone)
		}
	}

	log.Printf("done: %d inserted, %d skipped (of %d).", inserted, skipped, len(users))
}
